// Google Fonts CSS import string
export const googleFontsImport = fontFamilies
  .filter(font => font.googleFont)
  .map(font => {
    const weights = font.variants.map(v => v.weight).join(',');
    const italics = font.variants.some(v => v.style === 'italic') ? `:ital,wght@0,${weights};1,${weights}` : `:wght@${weights}`;
    return font.name.replace(/\s+/g, '+') + italics;
  })
  .join('&family=');

export const googleFontsUrl = `https://fonts.googleapis.com/css2?family=${googleFontsImport}&display=swap`;
